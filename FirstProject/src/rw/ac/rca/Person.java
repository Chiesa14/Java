package rw.ac.rca;

public class Person {

        private int age;
        private  String fname;
//Generating setters and getters
    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getFname() {
        return fname;
    }

    public void setFname(String fname) {
        this.fname = fname;
    }
}
